void Ship::setShipType(char _type)
{
	type = _type;
	health = _type;
}
//---------------------------------------------------------------------------

char Ship::getShipType()
{
	return type;
}
//---------------------------------------------------------------------------

void Ship::decreaseHealth()
{
	health--;
}
//---------------------------------------------------------------------------

int Ship::getHealth()
{
	return health;
}
//---------------------------------------------------------------------------

void Ship::addShipCoordinates(Cell* cell)
{
	coordinates.push_back(cell);
}
//---------------------------------------------------------------------------

vector <Cell*> Ship::getShipCoordinates()
{
	return coordinates;
}
